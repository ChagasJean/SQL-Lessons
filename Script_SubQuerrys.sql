/* VENDEDOR QUE VENDEU MENOS EM MARÇO E SEU NOME */

SELECT MIN(MARCO) FROM VENDEDOR;

SELECT NOME, MARCO FROM VENDEDOR
WHERE MARCO = (SELECT MIN(MARCO) FROM VENDEDOR);

/* VENDEDOR QUE VENDEU MAIS EM MARÇO E SEU NOME */

SELECT MAX(MARCO) FROM VENDEDOR;

SELECT NOME, MARCO FROM VENDEDOR
WHERE MARCO = (SELECT MAX(MARCO) FROM VENDEDOR);

/* QUEM VENDEU MAIS QUE O VALOR MÉDIO DE FEVEREIRO */

SELECT AVG(MARCO) FROM VENDEDOR;

SELECT NOME, MARCO FROM VENDEDOR
WHERE MARCO > (SELECT AVG(MARCO) FROM VENDEDOR);